# The return statement can contain an expression to execute once the function is called.

# I defined a function named multiplyFunction and passed it "num1" as an argument.
def multiplyFuntion(num1):
    return num1 * 5 # I used the return keyword to specify that I want num1 to be multiplied by 5

# I called the function, passed different numbers into it as the value for the num1 argument

print(multiplyFuntion(5))
print(multiplyFuntion(7))
print(multiplyFuntion(9))
